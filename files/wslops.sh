#!/bin/sh
# -----------------------------------------------------------------------------
#
# NAME
#     CMD - Wsl Operation Manager
#
# DESCRIPTION
#     Tool for executing one or multiple operations on the system. Default ones
#     are actually defined to clean up the system from sensitive or undesirable 
#     information and preparing it as a base for cloning, or instantiation of 
#     WSL instances, or to shrink the instance disk.
#     User can add new operations by filling the formatted set_operation_dictionary
#     string argument and implement them on the custom_operations section.
#
# USAGE
#     CMD --help
#         This help
#     CMD --version
#         It will display this script current version
#     CMD --list
#         It will list all supported operations and mark the default one with the
#         asterisk '*'.
#     CMD [--operations <operation-list>]... [--<key>=<val>]...
#         [--yes] [--strict] [--ignore-errors] [--verbose]  
#
#     '--operations'
#         Allow to set a comma separated list of operations where amongst the
#         actual names of the operations. Specials keywords 'default' and 'all'
#         are also recognized. Their meaning is obvious.
#         For the list of all supported operations use the '--list' option.
#     '--<key>=<val>'
#         Operation argument defined by the code 'arguments_get'
#         User can pass arguments to opérations by setting them on command line.
#         Array can be manage with pattern like '--arg-my-variable'. This variable
#         could be retreive on operation by requiering the following code:
#         'arguments_get $(arguments_key_format "arg" "my_variable")'
#     '--yes'
#         Never ask for permission - run non-interactively - otherwise in some
#         circumstances (for your own safety) program will refuse to continue
#         until you type 'yes'.
#     '--strict'
#         Abort the run of this program if system is not recognized.
#     '--verbose'
#         Print executed commands and show their output.
#
# EXAMPLES
#     CMD --operations default --yes
#         Run all default operations but only if explicitly replied 'YES'
#     CMD --operations default,-backup-files
#         Run all default operations EXCEPT the backup-files
#     CMD --operations backup-files,shell-history
#         Run only these two operations
#     CMD --operations all --yes
#         Run all default operations - no questions asked
#     CMD --operations user-account --username=anonymous
#         Run the create user operation with username as argument
#     CMD --operations ini-cfg --file=wsl.conf --ini-network-hostname=k8s \
#         --ini-automount-options='"metadata,uid=1000"'
#         Set the hostname in section network and options in the automount
#         section on the wsl.conf INI file
#
# -----------------------------------------------------------------------------

set -e

# shellcheck disable=SC1007
SCRIPTDIR=$(CDPATH= cd -- "$(dirname -- "$0")" && pwd)   
CMD=$(basename "$0")
VERSION="2.3.7"

# -----------------------------------------------------------------------------
#                     Operations Trigger Definitions
# -----------------------------------------------------------------------------

custom_operations() {

    # here are declared all operations - keep the following format:
    #   <operation>:<default>:<comment>
    # for each operation there should be corresponding function:
    #   op_<operation-with_underscores>
    set_operation_dictionary '
        system-update           :0: Update System
        backup-files            :1: Remove editor backup files from the guest
        crash-data              :1: Remove the crash data generated by kexec-tools
        customize               :1: Allow user defined default actions.
        logfiles                :1: Remove many log files from the guest
        mail-spool              :1: Remove email from the local mail spool directory
        package-manager-cache   :1: Remove package manager cache
        passwd-backups          :1: Remove /etc/passwd- and similar backup files
        prune-docker            :1: Remove all unused containers, networks, images and anonymous volumes 
        shell-history           :0: Remove the .history file
        tmp-files               :1: Remove temporary files
        user-account            :0: Set the default user account in the guest (require --username=<name>)
        ini-cfg                 :0: Fill INI configuration file (require --file=<file> [--ini-<key>=<value>]*)
        '


    # shellcheck disable=SC2329
    op_system_update(){
        run_op() {
            echo "+ ${*}"
            "$@"
        }

        if command -v apk >/dev/null; then
            run_op apk --update-cache upgrade
        elif command -v apt-get >/dev/null; then
            DEBIAN_FRONTEND=noninteractive
            export DEBIAN_FRONTEND
            run_op apt-get -qy update
            run_op apt-get -qy \
                -o Dpkg::Options::="--force-confdef" \
                -o DPkg::Options::="--force-confold" \
                upgrade
            run_op apt-get -qy autoremove
        elif command -v dnf >/dev/null; then
            run_op dnf -y --best upgrade
        elif command -v yum >/dev/null; then
            run_op yum -y --obsoletes update
        elif command -v zypper >/dev/null; then
            run_op zypper --non-interactive update --auto-agree-with-licenses
        elif command -v pkg >/dev/null; then
            # TODO: FreeBSD returns error when no update is available...
            run_op freebsd-update --not-running-from-cron fetch || true
            run_op freebsd-update --not-running-from-cron install || true
        else
            warn "No package manager found - SKIP"
        fi
    }

    # User defined operation
    # shellcheck disable=SC2329
    op_customize() (
        if [ -x "$SCRIPTDIR/$CMD-customize" ]; then
            run_op "$SCRIPTDIR/$CMD-customize" cleanup
        else
            echo "Not implemented"
        fi
    )

    # shellcheck disable=SC2329
    op_backup_files() (
        run_op() {
            echo "+ ${*}"
            "$@"
        }
        # do not search on mount partitions
        flag="-xdev"
        # shellcheck disable=SC2185
        if find --version 2>&1 | grep -q GNU; then
            flag="-mount"
        fi
        run_op find / "$flag" \( -name '*.bak' -o -name '*~' \) -delete || true
    )

    # shellcheck disable=SC2329
    op_crash_data() (
        run_op() {
            echo "+ ${*}"
            "$@"
        }
        run_op rm -rvf /var/crash/* /var/log/dump/*
    )

    # shellcheck disable=SC2329
    op_logfiles() (
        run_op() {
            echo "+ ${*}"
            "$@"
        }

        # Delete all .gz and rotated file
        run_op find /var/log -type f -regex ".*\.gz$" --delete
        run_op find /var/log -type f -regex ".*\.[0-9]$" --delete
        # set other file to empty
        run_op find /var/log/ -type f -exec cp /dev/null {} \;
    )

    # shellcheck disable=SC2329
    op_mail_spool() (
        run_op() {
            echo "+ ${*}"
            "$@"
        }
        run_op rm -rvf /var/spool/mail/* /var/mail/*
    )

    # shellcheck disable=SC2329
    op_package_manager_cache() (
        run_op() {
            echo "+ ${*}"
            "$@"
        }

        if command -v apk >/dev/null; then
            run_op apk cache clean
        elif command -v apt-get >/dev/null; then
            run_op apt-get clean
        elif command -v apt >/dev/null; then
            run_op apt clean
        elif command -v dnf >/dev/null; then
            run_op dnf clean all --enablerepo=\*
        elif command -v yum >/dev/null; then
            run_op yum clean all --enablerepo=\*
        elif command -v zypper >/dev/null; then
            run_op zypper clean --all
        elif command -v pkg >/dev/null; then
            run_op pkg clean --all --yes
        fi

        for d in \
            /var/cache/apt/archives/ \
            /var/cache/dnf/ \
            /var/cache/yum/ \
            /var/cache/zypp* \
            /var/cache/apk/*; do
            if [ -d "$d" ]; then
                run_op find "$d" -type f -delete
            fi
        done
        if command -v snap; then
            snap list --all | awk '/disabled/{print $1, $3}' |
                while read -r snapname revision; do
                    run_op snap remove "$snapname" --revision="$revision"
                done
        fi
    )

    # shellcheck disable=SC2329
    op_passwd_backups() (
        run_op() {
            echo "+ ${*}"
            "$@"
        }

        run_op rm -vf \
            /etc/group- \
            /etc/gshadow- \
            /etc/passwd- \
            /etc/shadow- \
            /etc/subuid- \
            /etc/subgid- \
            ;
    )

    # shellcheck disable=SC2329
    op_tmp_files() (
        run_op() {
            echo "+ ${*}"
            "$@"
        }

        for d in /tmp/ /var/tmp/; do
            if [ -d "$d" ]; then
                run_op find "$d" -maxdepth 1 -mindepth 1 -exec rm -rvf '{}' \;
            fi
        done
    )

    # shellcheck disable=SC2329
    op_shell_history() (
        run_op() {
            echo "+ ${*}"
            "$@"
        }

        run_op rm -vf \
            /home/*/.history \
            /root/.history \
            /home/*/.*_history \
            /root/.*_history \
            ;
    )

    # shellcheck disable=SC2329
    op_prune_docker() (
        run_op() {
            echo "+ ${*}"
            "$@"
        }
        if command -v docker >/dev/null; then
            run_op docker system prune --all --volumes --force
        fi
    )

  # shellcheck disable=SC2329
    op_ini_cfg() {
        file="$(arguments_get file)"
        arg_pattern="ini"
        if [ ! -n "$file" ]; then
            warn "No file specified in arguments - SKIP"
            return 2
        fi

        inicfg_args=
        keys="$(arguments_keys_starts_with "$arg_pattern")"
        for ini_key in $keys; do
            # transform to xpath
            key=$(arguments_key_format "$ini_key" "$arg_pattern" ".")
            inicfg_args="$inicfg_args $key=$(arguments_get "$ini_key")"
        done
        # shellcheck disable=SC2086
        system_set_inicfg "$file" $inicfg_args
    }

    # shellcheck disable=SC2329
    op_user_account() {
        run_op() {
            echo "+ ${*}"
            "$@"
        }

        os_id=$(system_distribution)
        username=$(arguments_get username)
        if [ ! -n "$username" ]; then
            warn "No username specified in arguments - SKIP"
            return 2
        fi

        if id "$username" >/dev/null 2>&1; then
            printf "[!] User %s already exists !\n" "$username"
            return 2
        fi

        grpname="$username"
        grpid=1000
        case "$os_id" in

            alpine)
                # install / configure sudo
                if ! command -v sudo >/dev/null; then
                    for pkg in sudo openssl shadow; do
                        if ! apk info 2>/dev/null | grep "$pkg" >/dev/null; then
                            run_op apk --no-cache add "$pkg"
                        fi
                    done
                    if ! grep sudo /etc/group; then
                        run_op addgroup --gid 65530 sudo
                    fi
                    run_op sed -i 's/# *%sudo/%sudo/' /etc/sudoers
                fi
                run_op addgroup --gid "$grpid" "$grpname"
                run_op adduser --disabled-password --gecos '' \
                    --uid 1000 -G "$grpname" "$username"
                run_op adduser "$username" sudo

                # Initialize user password:
                echo "Please create password for user $username"
                userencpass="$(/usr/bin/openssl passwd -1)"
                run_op usermod --password "$userencpass" "$username"
                return 0
                ;;

            ubuntu)
                # release >ubuntu-23 already has user ubuntu with uid:gid set to 1000:1001
                if id -u ubuntu >/dev/null 2>&1; then
                    run_op deluser --remove-home ubuntu >/dev/null
                fi
                if grep ':1000:' /etc/group >/dev/null; then
                    grpid=1001
                fi
                run_op addgroup --gid "$grpid" "$grpname"
                run_op adduser --quiet --disabled-password --gecos '' \
                    --uid 1000 --gid "$grpid" "$username"
                run_op usermod -aG sudo "$username"

                # Initialize user password:
                echo "Please create password for user $username"
                userencpass="$(/usr/bin/openssl passwd -1)"
                run_op usermod --password "$userencpass" "$username"
                return 0
                ;;

            #altlinux)
            #debian|devuan) ;;
            #fedora|centos|rhel|almalinux|ol|rocky|amzn) return 0 ;;
            #opensuse*|sles|sled) return 0 ;;
            #freebsd) return 0 ;;
            *)
                warn "This system ('${os_id:-UNKNOWN}') is not supported!"
                exit 2
                ;;
        esac
    }

}

